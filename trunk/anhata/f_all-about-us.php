<h2>informations sur nous</h2>
<p>Le nom: ANAHATA</p>
<p>Chacun possède sept chacres. Le chacre du coeur se nomme en Sanskrit
"ANAHATA".</p>
<p>Ce chacre est considéré comme centre de l’amour cosmique. Il s’agit
aussi du centre de rééquilibrage et de balance des chacres inférieurs (
avec accentuation physique ) et les chacres supérieurs ( avec
accentuation spirituelle ). Il est considéré aussi comme centre de
rotation d’une balance. Les personnes avec accent sur ce chacre vivent
dans une harmonie équilibrée, avec un amour pout tout ce qui leur
rencontre.</p>
<p>Les énergies du coeur sont amour et douleur. Au niveau du coeur il
est important de prendre conscience de ses actions et d’affiner la
formulation verbale de ses pensées et sentiments.</p>
<p>L’élément du ANAHATA est l’air et ses couleurs sont le vert clair
ainsi que le rosé clair.</p>
<p>Pour notre travail humanitaire a Puttaparthi et les allentours, cet
amour équilibré est très important. Notre engagement pour les enfants et
les personnes agées amène aussi un rééquilibrage entre les générations,
un contacte poli et gentil. Les respect de la nature et des ressources
existentes devient de plus en plus important. Aux Indes de ces jours, la
structure familiale des grandes familles se perd aussi de plus en plus.
Le pays agricole ne suffit plus a alimenter les petits fermiers. La
classe moyenne avec ses artisans disparait dans les petits villages pour
émigrer vers les villes. Ceci emmène la pauvreté et des itinérants.</p>
<p>L’association ANAHATA s’est toujours occupé du soulagement de la
détresse, par exemple la construction d’une maison de retraite pour
personnes agées à Puttaparthi. Grace a l’aide de sponsors indiens, la
maison de retraite n’a plus besoin de support financier de l’Allemagne.
Depuis dix années on peut aussi maintenir l’alimentation des pauvres de
la région. Nous avons toujours un contacte hebdomadaire avec cet ancien
projet a l’aide de dons pour alimentation (Narayena Seva) et par le
contacte avec Mme Ellionor Rahm qui gère une école a coudre pour jeunes
filles qui peuvent fréquenter ce cours gratuitement.</p>
<p>ANAHATA s’occupe aussi de la formation scolaire des enfants du cadre
et ancien cadre puisque les enfants sont le capital pour le futur des
familles et de toute la nation.</p>
<p>Nous supportons des écoles et nous accordons des prêts sans intérêts
a des écoliers pour leur donner la possibilité de fréquenter des
collèges et universités.</p>
<p>Un aspect important de notre travail est le soutien de l’aide à
soi-même dans le but que les projets deviennent autonomes le plus vite
que possible et n’ont plus besoin de support financier de notre part.
Ainsi nous pouvons envisager et innitier des nouveaux projets.</p>
