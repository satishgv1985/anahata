<h1>Propreté et création d’espaces verts</h1>
<p style="text-align: justify; padding: 5px;">Il s’agit d’un programme
formidable pour reconstituer la splendeur des villages anciens. L’usage
excessif de plastique ainsi que les déchéts industriels ont abouti à une
situation désolable, intolérable et dangereuse pour les prochaines
générations. Même pour notre génération il s’agit déjà d’une question de
survie.</p>
<p style="text-align: justify; padding: 5px;">Dans la mesure de notre
projet ECO CLUB nous voulons visiter les écoles des villages voisins et
faire de notre mieux pour convaincre les élèves de la nécessité d’un
environnement propre et d’espaces verts.</p>
<p style="text-align: justify; padding: 5px;">Une campagne de conscience
intensive fera tout pour éviter le désastre pour notre terre. Une équipe
travaillera sur lieu pour nettoyer et déblayer les déchéts.</p>
<p>Chaque village va reçevoir une formation au sujet de la création
d'espaces verts. En particulier nous voulons éducer les enfants et les
enseigner sur la nécessité de l’existence d’arbres et leur apprendre de
les planter aux bords des chemins et des routes dans les regions
pauvres.</p>
