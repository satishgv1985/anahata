		<p style="text-align: justify; padding: 5px;">La moitié des femmes à
		Siddarampuram, un petit village perdu en Inde méridionale, dans l’état
		d’Andhra Pradesh, dans le district d’Anantapur , participe activement
		au projet « Buffles », un projet de notre nouvelle organisation
		„FRIENDS“ fondée dans l’intention de donner du support aux femmes afin
		de les aider à satisfaire leurs besoins élémentaires. Ce projet,
		motivé par les villageois eux-mêmes, fait don de deux vaches à lait à
		des individus démunis sélectionnés. Le lait très riche de ces vaches
		leur assure un gagne-pain valable. <br />
		Chacune de ces 24 familles reçoit une installation pour produire du
		gaz biologique à partir du fumier produit principalement par les
		vaches. <br />
		Ce gaz à son tour servira à la cuisson des aliments, et le résidu du
		compost sera transformé en terre fertile. <br />
		Un effet secondaire, inattendu mais réjouissant, fut que les maris,
		pourtant buveurs invétérés, sont venus au secours de leurs épouses par
		tous les moyens, par exemple en construisant des abris pour les vaches
		devant les habitations. <br />
		Les autorités du village sont enthousiastes et elles attendent
		impatiemment la venue d’autres projets, tels l’installation de lampes
		solaires pour éclairer les rues, assurant ainsi la securité nocturne
		des villageoises. L’Inde en effet souffre encore et toujours de
		coupures de courant systématiques et prolongées, surtout en région
		rurale.</p>
