                    <h1>
                        Pouvoir des Femmes – Projet de modèle à Siddarampuram
                    </h1>
                    <p style="text-align: justify; padding: 5px;">
                       Dans un village  isolé de la plus basse caste où tous les hommes sont des alcooliques, presque la moitié des femmes se sont réunies dans un groupe d’aide (SHG. Selbsthilfegruppen) et de secours au sujet de leur propre cause. Elles ont organisé des réunions hebdomadaires et elles disposent d’un petit salaire d’appoint en confectionnant des sachets en papier. Elles ont leur propre compte d’épargne et elles apprennent a le gérér avec le soutien d’un comptable professionnel. Tous les membres du SHG respectent les principes suivants: pas de mariages d’enfants, pas de travail d’enfants, pas de violance a domicile et pas de gaspillage de ressources.
                    </p>
                    <p style="text-align: justify; padding: 5px;">
                        Les réunions hebdomadaires constituent une base pour se confier et discuter les problèmes sociaux, légaux,  politiques et  financiers. Les sujets de discussion sont entre autres: propriété privée, le droit de vote en ce qui concerne l’élection des instituteurs et des assistants  sociaux.  Ceci  dans le but de garantir aussi  le recours aux  droits de la  femme dans la cas de violence à domicile. Entre autre un cours de six mois est evisagé pour enseigner les femmes à coudre.
                    </p>
                    <p style="text-align: justify; padding: 5px;">
                        Les femmes discuttent aussi au sujet du planning familial, comme par exemple le nombre d’enfants ainsi que l’écart entre les naissances. Le contrôle de naissances es un changement très important dans les familles.
                    </p>
                    <p style="text-align: justify; padding: 5px;">
                        Aussi, elles n’ont pas hésité d’intégrer dans leur programme des sujets comme la violance a domicile, la traite ainsi que l’abus de femmes et d’ enfants.
                    </p>
